package com.arquitercturajava.bucles;

public class p06DoWhile {

	public static void main(String[] args) {
		// LO que cambia es que la primera i siempre se hará, esté o no dentro del while. 
		// Cambiar la i de valor para ver como funciona el bucle do while
		int i=84;
		do {
			System.out.println(i);
			i++;
		} while (i<=10);

	}

}
