package com.arquitecturajava.basicos;

public class p08Comparadores {

	public static void main(String[] args) {
		
		int x= 5;
		int y= 7;
		int z= 5;
		// IGual a se obtiene con ==
		// Distinto a se obtiene con !=
		// mayor que > menor que <
		System.out.println(x==y);
		System.out.println(x!=y);
		System.out.println(x==z);
		System.out.println(x!=z);
		System.out.println(x<y);
		System.out.println(x>y);
		
		

	}

}
