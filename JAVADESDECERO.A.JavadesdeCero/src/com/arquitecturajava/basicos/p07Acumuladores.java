package com.arquitecturajava.basicos;

public class p07Acumuladores {

	public static void main(String[] args) {
		
		int nota= 5;
		
		System.out.println(nota);
		nota++;
		//sumar a la variable un 1, seria igual a nota+1, pero comprimido se hace variable++;
		System.out.println(nota);
		nota--;
		//restar a la variable un 1, seria igual a nota-1, pero comprimido se hace variable--;
		System.out.println(nota);
	}

}
