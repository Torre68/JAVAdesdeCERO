package com.arquitecturajava.basicos;

public class p05variablesycopias {

	public static void main(String[] args) {


		int nota = 5;
		//puedo asignar un mismo valor a dos variables
		// Operador "=" es el operador de asignacion
		
		int nota2= nota;
		int nota3= nota2*5;
		
		System.out.println(nota2);
		System.out.println(nota3);
	}

}
