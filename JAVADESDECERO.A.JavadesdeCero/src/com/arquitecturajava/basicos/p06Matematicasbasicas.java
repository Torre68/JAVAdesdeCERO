		// Operadores matematicos basicos; -,+,*,/
		
		double nota1= 3.54;
		double nota2= 4.44;
		float nota3= 9.78f;
		float nota4= 6.87f;
		//Acordarse con variable floar hay que acompa√±ar al num de la "f" al final.
